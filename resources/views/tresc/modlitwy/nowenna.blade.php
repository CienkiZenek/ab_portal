<p class="tytul">Dzień drugi</p>
<p class="pod-tytul">Modlitwa o wierność Chrystusowi narodu polskiego</p>
<p class="akapit">Boże, twórco i władco narodów, któryś każdemu ludowi określił miejsce na ziemi, wyposażył go w
    odpowiednie dary, powierzył wszystkie ludy opiece swoich Aniołów: któryś naród polski przed tysiącem lat wprowadził
    do swojej owczarni, w królestwie swoim tu na ziemi wyznaczył mu zaszczytną rolę „przedmurza" i obrońcy, jak nasi
    ojcowie, chylimy dziś w pokorze swe czoła przed tronem Twego Majestatu i Miłosierdzia. Panie Zastępów, któryś
    prowadził Izraela w ognistym słupie do ojczystej ziemi, a zstępując w obłoku na skrzydłach złocistych cherubów,
    osłaniających Arkę Przymierza, sposobiłeś jego szeregi do zwycięskich bojów o cześć i chwałę Twego Imienia zwróć
    łaskawie swój wzrok na te pobojowiska, gdzie kładły się pokotem hufce polskich rycerzy i polska lała się krew w
    obronie krzyża i wiary ... na te pola: Legnicy i Warny - Chocimia i Wiednia Radzymina i Warszawy... Ofiarniku Boski,
    Jezu Chryste, oddający swe życie na drzewie krzyża za grzechy świata - oto nasza mała cząstka w kielichu Twej
    gorzkiej męki i krwawej śmierci... z polskiej ziemi męczeńskie kwiaty: Wojciech, Stanisław- Sadok z towarzyszami,
    Henryk Pobożny ze swymi zastępami - uniccy obrońcy wiary... a przede wszystkim zespolona z Twoją męką i zbawczą
    krwią ta drogocenna, obfita krew sługi Twego, ta męka Twego wiernego naśladowcy i ucznia, świętego Andrzeja Boboli.
    Niech będzie Imię Twoje, Panie, błogosławione w naszej chwale i w naszym poniżeniu, w niewoli i wyzwoleniu naszego
    narodu! Niech tak, jak kiedyś i dziś naród polski będzie przygotowany i chętny do walki na cześć Twego Imienia.
    Niech i dziś naród nasz będzie zdolny do wyśpiewania hymnu na Twoją chwałę, hymnu pisanego dziejami całych pokoleń.
    Niech dziś i zawsze będzie gotowy do ofiarnej obrony świętych Bożych znaków, wiekuistej prawdy i Twoich praw! Nasz
    Patronie, świadku naszych utrapień, męczeński drogowskazie naszego narodowego powołania i zwycięstwa, prowadź naród
