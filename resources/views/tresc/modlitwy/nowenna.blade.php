<p class="tytul">Dzień dziewiąty</p>
<p class="pod-tytul">Modlitwa o życie w łasce Bożej i o jej pomoc dla każdej duszy</p>
<p class="akapit">Na koniec naszej Nowenny, Boże nasz, mamy ostatnią prośbę najważniejszą: aby każdy z nas wierzył
    mocno i nieustraszenie, swoje życie stosował do nakazów tej wiary, ustawicznie żył w łasce uświęcającej. Naucz nas,
    abyśmy życie nadprzyrodzone w sobie bardzo cenili, zwłaszcza, gdy przyjdzie wybierać między zyskiem materialnym,
    grzeszną rozkoszą., a Twoją łaską, abyśmy nie sprzedawali dziecięctwa Bożego za miskę soczewicy lub jeszcze taniej.
    Pozwól, abyśmy nigdy nie przestawali być świątynią i mieszkaniem Ducha Bożego. Nie dopuszczaj, abyśmy mieli
    podzielić los zwiędłych i uschłych latorośli, odciętych i odrzuconych. Od grzechów śmiertelnych, zabijających w
    duszy życie Boże - uchowaj nas Panie! Od pokus ponad nasze słabe siły - wybaw nas, Panie! Niech w Twej łasce
    wzrastamy i pomnażamy ją przez modlitwę, dobre czyny, a przede wszystkim przez godne przyjmowanie Sakramentów
    świętych! Niech, gdy zawołasz, ten świat opuszczamy zjednoczeni z Tobą przez łaskę, zaopatrzeni na drogę wieczności!
    A że jesteśmy grzeszni, słabi i nędzni, niech nam towarzyszy ustawicznie Twa pomoc. Umysł osłabiony przez ziemskie i
    grzeszne ciemności, oświecaj światłem Twej łaski! Wzmacniaj Twą łaską chęć do dobrego, paraliżowaną już od młodości
    przez namiętności, pokusy, chęci użycia, bogactwa i pychy. Serce dążące do świata, ludzi i dóbr doczesnych, a może
    już w nich utkwione, pociągaj swą łaską ku sobie, gdzie można znaleźć jedyny i prawdziwy pokój ! Matko Łaski Bożej,
    Twoje miłosierne oczy na nas zwróć, w życiu każdego z nas osłaniaj nieustającą swą opieką, a Jezusa, błogosławiony
    owoc żywota Twojego, po tym wygnaniu nam okaż, o łaskawa, o litościwa, o słodka Panno Maryjo! Święty Andrzeju,
    szafarzu łask Bożych; za życia, w śmierci i po zgodnie, przez Twe wstawiennictwo o jedną największą łaskę błagamy:
    abyśmy, mimo naszych grzechów, okazali się godnymi współczucia i miłości Serca Bożego tu na ziemi, przy śmierci i w
    wieczności. </p>
