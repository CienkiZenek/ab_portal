<p class="tytul">Dzień pierwszy</p>
<p class="pod-tytul">Modlitwa o rozszerzenie i jedność Kościoła świętego</p>
<p class="akapit">Boski Założycielu Kościoła świętego, któryś go uczynił „kolumną i utwierdzeniem prawdy", bo poza nim
    niepewność i zwątpienie:</p>
<ul class="list-unstyled">

    <li class="mb-1 ms-4"><i class="fas fa-play text-warning me-2"></i> któryś chciał wszystkich ludzi, Krwią Twoją Najświętszą odkupionych jako Dobry Pasterz w jednej zgromadzić owczarni, bo poza nią tyle niebezpieczeństw czyhających na dusze ludzkie
    </li>

    <li class="mb-1 ms-4"><i class="fas fa-play text-warning me-2"></i> któryś Kościół święty w Ciało swe mistyczne ukształtował, by wszyscy z Tobą, Głową tego ciała złączeni, owoc przynosili na żywot wieczny
    </li>

    <li class="mb-1 ms-4"><i class="fas fa-play text-warning me-2"></i> spójrz: oto tak wiele dusz nie zna jeszcze nawet Imienia Twego, żyjąc w niewiedzy, że jest Ewangelia,

    </li>
    <li class="mb-1 ms-4"><i class="fas fa-play text-warning me-2"></i> spójrz: oto miliony oderwanych od „krzewu winnego", którym Ty sam jesteś, więdnie i schnie, jak „winne latorośle" odcięte,
    </li>
    <li class="mb-1 ms-4"><i class="fas fa-play text-warning me-2"></i> spójrz: oto fale przeciwności uderzają gwałtownie na opokę Piotrową, jak nigdy dotąd.
    </li>

</ul>
<p class="akapit">Nie darmo jednak przelałeś Krew swoją Najświętszą na krzyżu, aby użyźnić tę rolę i swą winnicę
    uprawić. Nie darmo z Krwią Twą Boską zmieszała się rzeka krwi męczeńskiej, płynąca od pierwszych wieków
    chrześcijaństwa, aż po dzień dzisiejszy: od tej krwi, która wsiąkła w piasek rzymskich aren, aż do tej, która
    skropiła ziemie Rosji, Meksyku, Hiszpanii, a nawet Polski i krajów misyjnych. Obficie przelał swą krew za Twój
    Kościół nieustraszony we wierze bojownik, święty Andrzej Bobola. Ubogaciłeś go, wywyższyłeś, uwielbiłeś. Niechże ta
    krew męczeńska stanie się nasieniem chrześcijan! Niech narody świata tchnieniem Twej łaski, Mocą Twego Słowa ze
    wschodu i zachodu zgromadzą się w Twoim królestwie! Niech na dźwięk Twego świętego Imienia zegnie się każde
    kolano... Niech się spełni jak najprędzej Twoje gorące pragnienie: by wszyscy, którzy wierzą w Ciebie, byli jedno,
    jako Ty w Ojcu, a Ojciec w Tobie, tak i oni w Ojcu i Synu stali się jedno. Niech ci, „którzy stanęli razem z
    władcami i panującymi tak licznie przeciw Panu i przeciw Chrystusowi", będą pokonani, a „pyszniący się zamysłami
    serc swoich niech będą rozproszeni"! Święty Andrzeju, za Oblubienicę Chrystusową Kościół katolicki wstawiaj się
    nieustannie u tronu Bożego.</p>
